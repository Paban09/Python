#creating list using []
a=[1,2,5,4,3,0]

#sorting the list
a.sort()
print("After sorting the list: ",a)

#reversing the list
a.reverse()
print("Reversig the list: ",a)

#appends at the end of the list
a.append('Pabon') 
print(a)

#insert in list at index 0 value 54
a.insert(0,54)
print(a)

#pop at index 2
a.pop(2)
print(a)

#remove a value
a.remove('Pabon')
print(a)